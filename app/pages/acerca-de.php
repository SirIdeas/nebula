<h1>Acerca de</h1>
<p>
  Proyecto desarrollado por Alexander Rondón (<a target="_blank" href="https://twitter.com/arondn2">@arondn2</a>) y Guillermo Seminario (<a target="_blank" href="https://twitter.com/gjseminario">@gjseminario</a>), asesorado por la Dra. Luz Marina Prada para optar al título de Ingeniería en Informática en la <a target="_blank" href="http://www.uneg.edu.ve/htmls/?t&p=portal.html">Universidad Nacional Experimental de Guayana (UNEG)</a>, bajo el nombre: <strong>PLATAFORMA PARA PRÁCTICAS DE SISTEMAS DE CONTROL CON DISPOSITIVOS MÓVILES INTELIGENTES ANDROID PARA EL LABORATORIO DE AUTOMATIZACIÓN INDUSTRIAL DE LA UNIVERSIDAD NACIONAL EXPERIMENTAL DE GUAYANA</strong> <a href="(:/:)/presentation/" target="_blank">(Ver presentación)</a>.
</p>

<h3>Resumen</h3>
<p>
  El Laboratorio de Automatización Industrial de la Universidad Nacional Experimental de Guayana, presenta dificultades al momento de desarrollar proyectos de automatización que involucren dispositivos móviles inteligentes; ya que no dispone de herramientas que permitan poner en práctica el desarrollo de proyectos sobre nuevas tecnologías como la telefonía móvil. La investigación tuvo como objetivo general proponer una plataforma de prácticas para proyectos de sistemas de control que involucren el uso de dispositivos móviles inteligentes con el sistema operativo Android. Para ello, se construyeron componentes de hardware aunado al desarrollo de la biblioteca de software del mismo. Entre las bases teóricas  tomadas como fundamentos se encuentra el trabajo realizado por Mazurov (2011-2012) sobre el uso de Arduino como USB anfitrión. La metodología utilizada fue la investigación tecnológica con un diseño de campo, recolectando información de las plataformas existentes en el Laboratorio de Automatización Industrial (UNEG); y a su vez se efectuó una revisión documental de manuales que usan y soportan dichas plataformas. Se aplicaron técnicas e instrumentos para la recolección de datos: encuestas, observación y análisis documental. Con el análisis obtenido, se derivaron las principales características y funcionalidades que debe poseer la propuesta. La investigación arrojó como resultado una plataforma de prácticas para proyectos de automatización y control con dispositivos móviles inteligentes basados en el sistema operativo Android, la cual está a disposición del Laboratorio de Automatización Industrial de la Universidad Nacional Experimental de Guayana, la cual cuenta con una nueva herramienta que facilita la creación de proyectos de automatización y control con dispositivos móviles inteligentes.
</p>
