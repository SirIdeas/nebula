<h1>Comenzar</h1>
<p>
  Nébula es un conjunto de retunias y clases encapsuladas en forma de biblioteca que busca facilitar el desarrollo de aplicaciones para Android que deben comunicarse con accesorios contruídos sobre placas de desarrollo Arduino o compatibles. Esto hace posible la integración de dispositivos Android en proyectos de domótica, control, seguridad, automatización e incluso robótica.
</p>
<p>
  Su fin principal servir de capa de abstracción de los diferentes tipos de comunicación soportadas entre los dispositivos Android y los accesorios. De igual forma ofrece variedad de clases representativas de gran varidad de componentes que usados frecuentemente en los proyectos con placas de desarrollo Arduino.
</p>
<p>
  De esta forma, haciendo uso de nébula se podrá desarrollar aplicaciones que puedan manipular y obtener información dispositivos de entrada y salida controlados por microcontroladores compatibles.
</p>
