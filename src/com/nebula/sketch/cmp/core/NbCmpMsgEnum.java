package com.nebula.sketch.cmp.core;

/**
 * Enumeración con los posibles eventos que se pueden generar en los componentes de entrada.
 * 
 * @version 1.0
 *
 */
public enum NbCmpMsgEnum {
	
	/**
	 * Cambio el estado de un componente.
	 */
	CHANGE_STATE,
	
	/**
	 * Un componente de entrada cambió de valor.
	 */
	CHANGE_VALUE,
	
}
